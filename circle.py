import math
'''Подключает библиотеку math'''

def area(r):
    '''Принимает число r - радиус'''

    return math.pi * r * r
    '''Возвращает площадь круга с радиусом r'''

def perimeter(r):
    '''Принимает число r - радиус'''

    return 2 * math.pi * r
    '''Возвращает длину окружности с радиусом r'''
